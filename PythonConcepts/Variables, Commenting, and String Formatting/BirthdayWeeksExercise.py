days_until_birthday = input("How many days are left until your birthday?")
approximate_num_of_weeks = int(days_until_birthday) / 7  # casting 'days_until_birthday' variable to int and finding the approximate number of weeks until the birthday.
print(approximate_num_of_weeks)
print(round(approximate_num_of_weeks, 3))  # with 3 figures precision.
