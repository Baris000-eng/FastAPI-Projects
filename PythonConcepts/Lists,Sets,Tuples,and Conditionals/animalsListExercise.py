zoo = ["cat", "parrot", "bear", "lion", "cheetah"]
zoo.pop(3)  # pops (or deletes) the animal at index 3 from the list called zoo.
zoo.append("rabbit")
zoo.pop(0)  # removes (or pops) the first animal, which has the index 0, from the list.
print(zoo)
print(zoo[0:3])  # It will print the first three animals from the list zoo.
